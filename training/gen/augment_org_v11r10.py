#!/usr/bin/env python3
"""v11r10 ORG-RECALL augmentation: close the organization/address recall gaps the egress re-stress surfaced on
v11r9c -- compound institution names ("Caisse Desjardins", "Mouvement Desjardins"), acronyms (IBM/MoMA/RAMQ),
ALLCAPS letterhead orgs, CamelCase-glued company names, street-name-WITHOUT-a-civic-number, and FR PO-box /
branch address fragments. The model already catches BARE single-token orgs; this teaches the multi-word /
acronym / letterhead forms it misses, in realistic Quebec FR + EN document contexts.

Lexicon (orgs / addresses / templates with {ORG}/{ADDR} / negatives) is generated by the v11r10-org-lexicon
workflow and saved to datasets/v11r10_lexicon.json. This script does the OFFSET-EXACT span placement that an LLM
cannot be trusted with. Rows match the suite schema: {"input", "output":{"spans":[[s,e,label]], "entities":{}}}.

Negatives (CAPITALIZED non-org phrases: API/CSV/section headers/...) carry NO span -- the precision guard so the
extra org coverage does not start nuking ordinary capitalized words.
"""
import argparse
import json
import os
import random
import re

_PH = re.compile(r'\{(ORG|ADDR)\}')


def _emit(text, spans, src="augment_org_v11r10"):
    for s, e, lab in spans:
        assert 0 <= s < e <= len(text), (s, e, len(text), repr(text))
    return {"input": text, "output": {"spans": [[s, e, lab] for s, e, lab in spans], "entities": {}},
            "meta": {"src": src}}


def _variant(rng, org):
    """Occasionally render an org in a leak-prone surface form: ALLCAPS (letterhead) or, for a multi-word name,
    a CamelCase-glued form. Mostly leave it as-is."""
    r = rng.random()
    if r < 0.15:
        return org.upper()
    if r < 0.22 and ' ' in org and org.isascii():   # glue only ascii multiword -> CamelCase
        return ''.join(w.capitalize() for w in org.split())
    return org


def _fill(template, rng, orgs, addrs):
    """Substitute every {ORG}/{ADDR} placeholder with a fresh sampled value, returning (text, spans) with
    offset-exact spans into the SUBSTITUTED text."""
    out = []
    spans = []
    i = 0
    result = ''
    for m in _PH.finditer(template):
        result += template[i:m.start()]
        if m.group(1) == 'ORG':
            val = _variant(rng, rng.choice(orgs))
            lab = 'organization'
        else:
            val = rng.choice(addrs)
            lab = 'address'
        start = len(result)
        result += val
        spans.append([start, len(result), lab])
        i = m.end()
    result += template[i:]
    return result, spans


_BARE_WRAP = ['{X}', '"{X}"', '- {X}', '{X}.', '* {X}', '{X} ', '   {X}']
_NEG_WRAP = ['{X}', '"{X}"', 'Voir {X} pour les détails.', 'Consultez la section {X}.',
             'See {X} for more information.', '- {X}', '{X}:', 'Référence: {X}']


def _record(rng, lex):
    orgs, addrs, templates, negs = lex['orgs'], lex['addresses'], lex['templates'], lex['negatives']
    r = rng.random()
    if r < 0.22:                                   # NEGATIVE: capitalized non-org phrase, NO span
        neg = rng.choice(negs)
        wrap = rng.choice(_NEG_WRAP).replace('{X}', neg)
        return _emit(wrap, [])
    if r < 0.38:                                   # BARE org (incl ALLCAPS/CamelCase variants)
        org = _variant(rng, rng.choice(orgs))
        wrap = rng.choice(_BARE_WRAP)
        pre = wrap[:wrap.index('{X}')]
        text = wrap.replace('{X}', org)
        return _emit(text, [[len(pre), len(pre) + len(org), 'organization']])
    if r < 0.50:                                   # ADDRESS-bearing template, or a bare address line
        addr_templates = [t for t in templates if '{ADDR}' in t['text']]
        if addr_templates and rng.random() < 0.7:
            text, spans = _fill(rng.choice(addr_templates)['text'], rng, orgs, addrs)
            if spans:
                return _emit(text, spans)
        a = rng.choice(addrs)
        return _emit(a, [[0, len(a), 'address']])
    # TEMPLATE with {ORG} (the main path)
    org_templates = [t for t in templates if '{ORG}' in t['text']]
    text, spans = _fill(rng.choice(org_templates)['text'], rng, orgs, addrs)
    if not spans:                                  # template had no placeholder -> fall back to bare org
        org = rng.choice(orgs)
        return _emit(org, [[0, len(org), 'organization']])
    return _emit(text, spans)


def build(n, lex, seed):
    rng = random.Random(seed)
    return [_record(rng, lex) for _ in range(n)]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--lexicon', default='training/gen/v11r10_lexicon.json')
    ap.add_argument('--out-dir', required=True)
    ap.add_argument('--n-train', type=int, default=6000)
    ap.add_argument('--n-val', type=int, default=600)
    args = ap.parse_args()
    lex = json.load(open(args.lexicon, encoding='utf-8'))
    lex['orgs'] = [o for o in lex['orgs'] if o.strip()]
    lex['addresses'] = [a for a in lex['addresses'] if a.strip()]
    lex['negatives'] = [n for n in lex['negatives'] if n.strip()]
    os.makedirs(args.out_dir, exist_ok=True)
    for split, n, seed in [('train', args.n_train, 11010), ('val', args.n_val, 11011)]:
        rows = build(n, lex, seed)
        path = os.path.join(args.out_dir, f'org_v11r10_{split}.jsonl')
        with open(path, 'w', encoding='utf-8') as fh:
            for row in rows:
                fh.write(json.dumps(row, ensure_ascii=False) + '\n')
        npos = sum(1 for r in rows if r['output']['spans'])
        print(f'{split}: {len(rows)} rows ({npos} with spans) -> {path}')


if __name__ == '__main__':
    main()
